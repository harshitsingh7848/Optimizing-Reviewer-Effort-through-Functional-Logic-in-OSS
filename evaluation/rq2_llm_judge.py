from radon.complexity import cc_visit

def cyclomatic_complexity_total(code: str) -> int:
    try:
        blocks = cc_visit(code)
        return sum(block.complexity for block in blocks)
    except Exception:
        return 0


def max_nesting_depth(code: str) -> int:
    try:
        tree = ast.parse(code)
    except SyntaxError:
        return 0

    class DepthVisitor(ast.NodeVisitor):
        def __init__(self):
            self.current_depth = 0
            self.max_depth = 0
        def _enter(self):
            self.current_depth += 1
            self.max_depth = max(self.max_depth, self.current_depth)
        def _exit(self):
            self.current_depth -= 1
        def visit_If(self, node):
            self._enter(); self.generic_visit(node); self._exit()
        def visit_For(self, node):
            self._enter(); self.generic_visit(node); self._exit()
        def visit_While(self, node):
            self._enter(); self.generic_visit(node); self._exit()
        def visit_Try(self, node):
            self._enter(); self.generic_visit(node); self._exit()

    v = DepthVisitor()
    v.visit(tree)
    return v.max_depth


LOGIC_NODES = (
    ast.If, ast.For, ast.While, ast.Try,
    ast.BoolOp, ast.Compare, ast.Return,
)

def logic_density(code: str) -> float:
    try:
        tree = ast.parse(code)
    except SyntaxError:
        return 0.0
    logic_lines = set()
    for node in ast.walk(tree):
        if isinstance(node, LOGIC_NODES) and hasattr(node, "lineno"):
            logic_lines.add(node.lineno)
            if isinstance(node, ast.If) and node.orelse:
                logic_lines.add(node.orelse[0].lineno)
    lines = [l for l in code.strip().split("\n") if l.strip()]
    return len(logic_lines) / len(lines) if lines else 0.0


