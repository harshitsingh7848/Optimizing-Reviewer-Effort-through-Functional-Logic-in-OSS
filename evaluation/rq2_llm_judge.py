def clean_raw_response(raw: str) -> str:
    if "<think>" in raw:
        raw = raw[raw.rfind("</think>") + len("</think>"):].strip()
    if "```" in raw:
        for part in raw.split("```"):
            part = part.strip()
            if part.startswith("json"):
                part = part[4:].strip()
            if part.startswith("{"):
                raw = part
                break
    return raw.strip()

def call_llm_judge_segment(seg: dict, pipe, tokenizer, model_name: str) -> dict:
    messages = [
        {"role": "user", "content": [
            {"type": "text", "text": SEGMENT_SYSTEM_PROMPT + "\n\n" + build_segment_user_message(seg)}
        ]}
    ]

    for attempt in range(3):
        try:
            is_gemma = "gemma" in model_name.lower()

            if is_gemma:
                # Official Gemma3 pattern from HuggingFace
                inputs = tokenizer.apply_chat_template(
                    messages,
                    add_generation_prompt=True,
                    tokenize=True,
                    return_dict=True,
                    return_tensors="pt",
                ).to(pipe.model.device)
                with torch.no_grad():
                    output_ids = pipe.model.generate(
                        **inputs,
                        max_new_tokens=256,
                        do_sample=False,
                    )
                raw = tokenizer.tokenizer.decode(
                    output_ids[0][inputs["input_ids"].shape[-1]:],
                    skip_special_tokens=True
                ).strip()
            else:
                # Standard pipeline call for Qwen and Llama
                flat_messages = [
                    {"role": "user", "content": SEGMENT_SYSTEM_PROMPT + "\n\n" + build_segment_user_message(seg)}
                ]
                outputs = pipe(
                    flat_messages,
                    max_new_tokens=256,
                    temperature=0.1,
                    do_sample=True,
                    pad_token_id=tokenizer.eos_token_id,
                    max_length=None,   # override the model's default max_length
                )
                result = outputs[0]["generated_text"]
                if isinstance(result, list):
                    raw = result[-1]["content"].strip()
                elif isinstance(result, str):
                    raw = result.strip()
                else:
                    raw = str(result).strip()
                # raw = outputs[0]["generated_text"][-1]["content"].strip()

            raw = clean_raw_response(raw)
            try:
                return json.loads(raw)
            except json.JSONDecodeError:
                if attempt == 2:
                    print(f"  [WARN] Parse failed for {model_name}: {raw[:120]}")
                    return {"risk_score": None, "rationale": raw}
                continue

        except Exception as e:
            print(f"  [ERROR] Attempt {attempt+1}/3 failed for {model_name}: {e}")
            if attempt < 2:
                time.sleep(5)
            else:
                return {"risk_score": None, "rationale": str(e)}

def compute_rf_baseline_rq2(segments: list, gt_labels: list):
    X = np.array([[
        s.get("cyclomatic_complexity", 0),
        s.get("nesting_depth", 0),
        s.get("logic_density", 0.0),
    ] for s in segments])
    y = np.array(gt_labels)
    idx = list(range(len(segments)))
    idx_train, idx_test = train_test_split(idx, test_size=0.2, random_state=42, stratify=y)
    rf = RandomForestClassifier(n_estimators=100, random_state=42)
    rf.fit(X[idx_train], y[idx_train])
    return rf.predict(X[idx_test]), y[idx_test], idx_test


def run_mcnemar(llm_preds, rf_preds, gt):
    a = sum(1 for l, r, g in zip(llm_preds, rf_preds, gt) if l == g and r == g)
    b = sum(1 for l, r, g in zip(llm_preds, rf_preds, gt) if l != g and r == g)
    c = sum(1 for l, r, g in zip(llm_preds, rf_preds, gt) if l == g and r != g)
    d = sum(1 for l, r, g in zip(llm_preds, rf_preds, gt) if l != g and r != g)
    print(f"  McNemar: both_correct={a}, llm_wrong_rf_right={b}, "
          f"llm_right_rf_wrong={c}, both_wrong={d}  (b+c={b+c})")
    result = mcnemar_test(np.array([[a, b], [c, d]]), exact=True)
    return result.pvalue, {"a": a, "b": b, "c": c, "d": d}


