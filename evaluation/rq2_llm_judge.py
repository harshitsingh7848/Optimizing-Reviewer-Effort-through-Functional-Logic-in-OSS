def load_model(hf_repo: str):
    print(f"\nLoading {hf_repo} ...")

    if "gemma-3" in hf_repo.lower() or "gemma3" in hf_repo.lower():
        from transformers import AutoProcessor, AutoModelForImageTextToText
        model = AutoModelForImageTextToText.from_pretrained(
            hf_repo,
            dtype=torch.bfloat16,
            device_map="auto",
            token=os.environ.get("HUGGING_FACE_HUB_TOKEN"),
        )
        class PipeWrapper:
            def __init__(self, m): self.model = m
        pipe = PipeWrapper(model)
    else:
        kwargs = dict(
            model=hf_repo,
            device_map="auto",
            return_full_text=False,
            token=os.environ.get("HUGGING_FACE_HUB_TOKEN"),
            dtype=torch.float16,
        )
        pipe = pipeline("text-generation", **kwargs)
        if pipe.tokenizer.pad_token is None:
            pipe.tokenizer.pad_token = pipe.tokenizer.eos_token

    print(f"  Loaded. VRAM used: {torch.cuda.memory_allocated() / 1e9:.2f} GB")
    return pipe

def unload_model(pipe):
    try:
        del pipe
        gc.collect()
        torch.cuda.empty_cache()
        free = torch.cuda.get_device_properties(0).total_memory - torch.cuda.memory_allocated()
        print(f"  Model unloaded. VRAM free: {free / 1e9:.2f} GB")
    except Exception as e:
        print(f"  Warning: unload error (non-fatal): {e}")
        gc.collect()


