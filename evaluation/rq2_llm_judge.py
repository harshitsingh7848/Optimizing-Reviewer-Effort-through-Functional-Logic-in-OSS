def get_inline_comments(pr_number: int, repo: str) -> list:
    """Fetch inline review comments for a PR. Returns list of {line, path} dicts."""
    url = f"https://api.github.com/repos/{repo}/pulls/{pr_number}/comments"
    comments, page = [], 1
    while True:
        try:
            resp = requests.get(url, headers=HEADERS,
                                params={"per_page": 100, "page": page}, timeout=30)
            resp.raise_for_status()
            batch = resp.json()
        except Exception:
            break
        if not batch:
            break
        for c in batch:
            line = c.get("original_line") or c.get("line")
            path = c.get("path", "")
            if line and path:
                comments.append({"line": line, "path": path})
        if len(batch) < 100:
            break
        page += 1
        time.sleep(0.3)
    return comments


def label_segment_inline(seg: dict, inline_comments: list) -> int:
    """
    Label = 1 if at least one inline comment is anchored within
    [start_line, end_line] of this segment's file.
    This is the ground truth described in the paper.
    """
    start = seg["start_line"]
    end   = start + seg.get("added_lines", 0) + seg.get("removed_lines", 0)
    fname = seg["filename"]
    return 1 if any(
        c["path"] == fname and start <= c["line"] <= end
        for c in inline_comments
    ) else 0



