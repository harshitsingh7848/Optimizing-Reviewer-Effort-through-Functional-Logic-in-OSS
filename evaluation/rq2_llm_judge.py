def parse_diff_into_segments(diff: str, pr: dict) -> list:
    if not diff or not diff.strip():
        return []

    segments = []
    current_file        = "unknown"
    current_hunk_header = ""
    current_lines       = []
    start_line          = 0

    def flush_hunk():
        if not current_lines:
            return
        added   = [l[1:] for l in current_lines if l.startswith("+")]
        removed = [l[1:] for l in current_lines if l.startswith("-")]
        context = [l[1:] for l in current_lines if l.startswith(" ")]
        snippet = "\n".join(added) if added else "\n".join(removed)
        if not snippet.strip():
            return

        name = current_hunk_header
        for line in added + context:
            m = re.match(r"^\s*(def|class|async def)\s+(\w+)", line)
            if m:
                name = m.group(2)
                break

        segments.append({
            "name"          : name,
            "filename"      : current_file,
            "start_line"    : start_line,
            "hunk_header"   : current_hunk_header,
            "code_snippet"  : snippet[:600],
            "added_lines"   : len(added),
            "removed_lines" : len(removed),
            "pr_id"         : pr.get("pr_id"),
            "pr_title"      : pr.get("title", ""),
        })

    for line in diff.splitlines():
        if line.startswith("diff --git"):
            flush_hunk()
            current_lines = []
        elif line.startswith("+++ b/"):
            current_file = line[6:].strip()
        elif line.startswith("@@"):
            flush_hunk()
            current_lines       = []
            current_hunk_header = line
            m = re.search(r"\+(\d+)", line)
            start_line = int(m.group(1)) if m else 0
        elif line.startswith((" ", "+", "-")) and not line.startswith("---"):
            current_lines.append(line)

    flush_hunk()
    return segments

